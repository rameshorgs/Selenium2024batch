package com.corejava;

public class BaseClass {

    public void print(String sentance){
        System.out.println(sentance);
    }
    public void print(int sentance){
        System.out.println(sentance);
    }

    public void print(int sentance, int sen1){
        System.out.println(sentance+"=="+sen1);
    }
    public void print(double sentance){
        System.out.println(sentance);
    }
    public void print(float sentance){
        System.out.println(sentance);
    }
}
